# Ciclo For, recorre los elementos o repite una cantidad de veces

# ciclo de rango maximo 5
for i in range(5):
    print(i)

# ciclo de rango de lista
for fruta in ["manzanza", "fresa", "platano"]:
    print(fruta)

# ciclo de rango de inicio y fin
for i in range(2, 5):
    print(i)

# ciclo de rango negativo y positivo
for i in range(-2, 2):
    print(i)